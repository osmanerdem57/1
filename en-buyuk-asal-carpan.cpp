#include <iostream>
using namespace std;
int main()
{
	setlocale(LC_ALL, "turkish");
	int sayi, i;
	cout << "bir değer giriniz: ";
	cin >> sayi;
	for (i = 2;i <= sayi;i++)
	{
		if (sayi % i == 0)
			cout << endl;
		while (sayi % i == 0)
		{
			cout << i << " ";
			sayi = sayi / i;
		}
		
		
	}
}

